            <div class="mb-12">
              <p class="text-gray-700 dark:text-gray-300 leading-relaxed mb-6">
                As part of this current engagement with SkySoft-ATM, Adeptus will be using their latest version of Evidence Capture solution which has been designed specifically for the Public Safety and Security market. The solution captures the operator's actions including keyboard inputs, mouse clicks and video streams sent to the large format video wall during their shift. With this level of recording being made available, Adeptus will be able to reconstruct the whole chain of actions that were taken during any particular incident and then consequently adapt their workflows and trainings to solve potential occurrences.
              </p>
