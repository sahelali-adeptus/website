                Adeptus Technologies LLC, a key player in the IoT and Managed Services domain in the UAE have recently implemented solutions from SkySoft-ATM, Switzerland – a market leader for recording solutions with deployments in the largest workstation recording projects, traditionally in the Air Traffic management domain globally. With this engagement, SkySoft-ATM are broadening their reach to now offer their recording expertise to the Public Safety and Security, IoT and Managed services domains specific to control room applications.
